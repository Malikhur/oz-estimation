// Helper functions
export function getCategoryBySlug(slug) {
  return serviceCategories.find(cat => cat.slug === slug);
}

export function getServiceBySlug(categorySlug, serviceSlug) {
  const category = getCategoryBySlug(categorySlug);
  if (!category) return null;
  return category.services.find(s => s.slug === serviceSlug);
}

export function getAllServiceParams() {
  const params = [];
  serviceCategories.forEach(cat => {
    cat.services.forEach(svc => {
      params.push({ category: cat.slug, service: svc.slug });
    });
  });
  return params;
}

export function getAllCategoryParams() {
  return serviceCategories.map(cat => ({ category: cat.slug }));
}
